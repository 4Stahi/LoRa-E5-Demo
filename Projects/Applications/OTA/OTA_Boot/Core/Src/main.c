uint32_t System_ParamUpdate(uint32_t addr, SystemParamTypeDef *pData)
{
  if(addr != SYSTEMPARAM_ADDR) //&& addr != SYSTEMPARAM_BACKUPADDR)
    return 1;

  if(FLASH_If_Erase(addr, 1) != 0)
    return 1;

  if(FLASH_If_Write(addr, (uint32_t*)pData, sizeof(SystemParamTypeDef) / 4) != HAL_OK)
    return 1;

    return 0;
}
uint32_t System_SelectBootAddr(SystemParamTypeDef *pData)
{
  uint32_t size = 0;
  FLASH_If_Read(SYSTEMPARAM_ADDR, (uint32_t*)pData, sizeof(SystemParamTypeDef) / 4);
  if(pData->App_Status > 1 || pData->App_Version == 0xFFFFFFFF)
  {	 /* 第一次烧录是写入默认信息 */
	 memcpy(pData, &SystemParam_default, sizeof(SystemParamTypeDef));
  }
  else if(pData->App_Version == TEST_VERSION)
  {
    /* 如果版本号为dddddddd，即更新后第一次运行失败 */
    pData->App_Status = 1 - pData->App_Status;
  }
  else if(pData->App_Version == UPDATE_VERSION)
  {
    pData->App_Status = 1 - pData->App_Status;
    Ymodem_Receive( &size );
    pData->App_Version = TEST_VERSION;
  }
  System_ParamUpdate(SYSTEMPARAM_ADDR, pData);
  //printf("\n\n\rApp_Status  1\n\r", &pData->App_Status);
  return APP_ADDR[pData->App_Status];
}
